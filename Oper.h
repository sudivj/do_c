float Add(float n1,float n2){
    float result; 
    result = n1 + n2;   
    return result;
}
float sub(float n1,float n2){
    float result;
    result = n1 - n2;
    return result;
}
float divide(float n1,float n2){
    float result;
    result = n1 / n2;
    return result;
}
float multiply(float n1,float n2){
    float result;
    result = n1 * n2;
    return result;
}